// 3. WAP to take number of month as user input 
// and print in words using switch case

public class q3_month {
    public static void main(String[] args) {
        int month = 12;

        switch (month) {

            case 1:

                System.out.println("january");
                break;

            case 2:

                System.out.println("fenruary");
                break;

            case 3:

                System.out.println("march");
                break;

            case 4:

                System.out.println("april");
                break;

            case 5:

                System.out.println("may");
                break;

            case 6:

                System.out.println("june");
                break;

            case 7:

                System.out.println("july");
                break;

            case 8:

                System.out.println("agust");
                break;

            case 9:

                System.out.println("sep");
                break;

            case 10:

                System.out.println("october");
                break;

            case 11:

                System.out.println("novermber");
                break;
                case 12:

                System.out.println("december");
                break;

        }
    }

}
